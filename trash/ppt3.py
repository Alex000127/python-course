# Solicitar al usuario que ingrese una cadena de texto en mayúsculas
cadena = input("Ingresa una cadena de texto en mayúsculas: ")

# Utilizar la función lower() para convertir la cadena a minúsculas
cadena_minusculas = cadena.lower()

# Imprimir la cadena en minúsculas en pantalla
print("La cadena en minúsculas es:", cadena_minusculas)