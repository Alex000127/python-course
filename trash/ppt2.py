# Solicitar al usuario que ingrese una cadena de texto en minúsculas
cadena = input("Ingresa una cadena de texto en minúsculas: ")

# Utilizar la función upper() para convertir la cadena a mayúsculas
cadena_mayusculas = cadena.upper()

# Imprimir la cadena en mayúsculas en pantalla
print("La cadena en mayúsculas es:", cadena_mayusculas)